function out = sqclr(map,id)
% map = color map to use, red = 'r', blue = 'b', blue to red 'r2b'
% id = color number

% reds
clr_r = {[255, 225, 100]/255, [255, 200, 100]/255, [255, 150, 50]/255, ...
    [255, 100, 50]/255, [255, 50, 50]/255, [225, 0, 50]/255, ...
    [175, 0, 0]/255};
% blue
clr_b = { [150, 200 , 255] / 255, [125, 170, 255]/255, [50, 150, 255]/255, ...
    [0, 110, 220]/255, [0, 100 ,175]/255, [0, 50, 100]/255, [0, 0, 50]/255 }; 
% clr_b = { [175, 175, 255] / 255, [100, 200, 255]/255, [50, 150, 204]/255, ...
%     [0, 100, 150]/255, [0, 50 ,100]/255, [0, 25, 100]/255 }; 
% blue2red
clr_b2r = [fliplr(clr_b), (clr_r)];

switch map
    case 'r'
        out = clr_r{id};
    case 'b'
        out = clr_b{id};
    case 'r2b'
        out = clr_b2r{id};
end
end




